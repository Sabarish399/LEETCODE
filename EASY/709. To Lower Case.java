// Problem: 709. To Lower Case
// Link: https://leetcode.com/problems/to-lower-case/
// Approach:
//   Use Java’s built-in String method to convert all characters to lowercase.
//   This directly handles ASCII letters as well as locale-independent cases.
// Time Complexity: O(n)
// Space Complexity: O(n) — creates a new string

class Solution 
{
    public String toLowerCase(String s) 
    {
        return s.toLowerCase();
    }
}
